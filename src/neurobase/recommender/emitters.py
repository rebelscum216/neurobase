"""Consent-neutral artifact rendering for Phase 8 Workstream G."""

from __future__ import annotations

from dataclasses import dataclass
from pathlib import Path
from typing import Any

import yaml

from neurobase.core import projects, redact, store
from neurobase.recommender import proposals


@dataclass(frozen=True)
class Artifact:
    path: Path
    before: str
    after: str
    target: str
    foreign: bool = False


def prepare(root: Path, doc: store.Document, *, skill_scope: str | None = None) -> Artifact:
    draft = proposals.extract_draft(doc.body)
    if draft is None:
        raise ValueError("proposal has no valid managed draft region")
    draft = redact.redact(draft)
    slug = str(doc.get("name") or "")
    kind = str(doc.get("type") or "")
    if kind == "skill":
        scope = skill_scope or ("user" if doc.get("target") == "user-skill" else "project")
        return _skill(root, doc, slug, draft, scope)
    if kind == "rule":
        return _rule(root, doc, slug, draft)
    raise ValueError(f"unsupported proposal type {kind!r}")


def _project_root(root: Path, doc: store.Document) -> Path:
    project = doc.get("project")
    if not isinstance(project, str) or not project:
        raise ValueError("this proposal has no single source project")
    roots = projects.load_registry(root).get(project)
    if not roots:
        raise ValueError(f"proposal project {project!r} is not registered")
    return Path(roots[0])


def _skill(root: Path, doc: store.Document, slug: str, draft: str, scope: str) -> Artifact:
    if scope not in {"user", "project"}:
        raise ValueError("skill target must be user or project")
    base = Path.home() if scope == "user" else _project_root(root, doc)
    path = base / ".claude" / "skills" / slug / "SKILL.md"
    before = path.read_text(encoding="utf-8") if path.exists() else ""
    foreign = bool(before) and not _owned_skill(before, slug)
    title = next((line[2:].strip() for line in draft.splitlines() if line.startswith("# ")), slug)
    body = (
        draft
        if any(line.startswith("# ") for line in draft.splitlines())
        else f"# {title}\n\n{draft}"
    )
    fm: dict[str, Any] = {
        "name": slug,
        "description": str(doc.get("candidate_type") or title),
        "neurobase_managed": True,
        "neurobase_slug": slug,
    }
    after = f"---\n{yaml.safe_dump(fm, sort_keys=False)}---\n\n{body.rstrip()}\n"
    return Artifact(path, before, after, f"{scope}-skill", foreign)


def _owned_skill(text: str, slug: str) -> bool:
    try:
        if not text.startswith("---\n") or "\n---\n" not in text[4:]:
            return False
        frontmatter, _body = text[4:].split("\n---\n", 1)
        fm = yaml.safe_load(frontmatter)
        return (
            isinstance(fm, dict)
            and fm.get("neurobase_managed") is True
            and fm.get("neurobase_slug") == slug
        )
    except (ValueError, yaml.YAMLError):
        return False


def _rule(root: Path, doc: store.Document, slug: str, draft: str) -> Artifact:
    target = str(doc.get("target") or "")
    if target not in {"AGENTS.md", "CLAUDE.md"}:
        raise ValueError(f"invalid rule target {target!r}")
    path = _project_root(root, doc) / target
    before = path.read_text(encoding="utf-8") if path.exists() else ""
    start = (
        f"<!-- neurobase:rule:{slug} (generated by `neurobase recommend accept` — "
        "hand edits inside this block are overwritten on the next accept of this proposal) -->"
    )
    end = f"<!-- /neurobase:rule:{slug} -->"
    block = f"{start}\n{draft.rstrip()}\n{end}"
    start_count = before.count(start)
    end_count = before.count(end)
    if start_count != end_count or start_count > 1:
        raise ValueError(f"target contains malformed or duplicate rule markers for {slug!r}")
    s = before.find(start)
    e = before.find(end, s + len(start)) if s >= 0 else -1
    if s >= 0 and e >= 0:
        after = before[:s] + block + before[e + len(end) :]
    else:
        heading = "## Neurobase-managed rules"
        addition = f"{heading}\n\n{block}\n" if heading not in before else f"{block}\n"
        separator = (
            ""
            if not before or before.endswith("\n\n")
            else ("\n" if before.endswith("\n") else "\n\n")
        )
        after = f"{before}{separator}{addition}"
    return Artifact(path, before, after, target)


def write_atomic(artifact: Artifact) -> None:
    artifact.path.parent.mkdir(parents=True, exist_ok=True)
    tmp = artifact.path.with_name(artifact.path.name + ".tmp")
    tmp.write_text(artifact.after, encoding="utf-8")
    tmp.replace(artifact.path)
